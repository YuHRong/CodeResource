console.log("Hello! I'm your coding fun fact guide!");

// 2. 创建三个变量
let botName = "CodeBot";
let botLocation = "the Cloud";
let favoriteLanguage = "JavaScript";

// 3. 输出机器人基本信息
console.log(`My name is ${botName} and I live on ${botLocation}.`);

// 4. 输出最喜欢的编程语言
console.log(`My favorite programming language is ${favoriteLanguage}.`);

// 5. 创建codingFact变量并赋值第一个有趣事实
let codingFact = `Did you know that ${favoriteLanguage} was created in just 10 days?`;
console.log(codingFact);

// 7. 重新赋值codingFact为第二个事实
codingFact = `${favoriteLanguage} is the most popular programming language according to GitHub's annual reports.`;
console.log(codingFact);

// 9. 再次重新赋值codingFact为第三个事实
codingFact = `One interesting fact about ${favoriteLanguage} is that it's not related to Java despite the name similarity!`;
console.log(codingFact);

