# 增强赋值运算符
a = 10
print("初始值a:", a)  # 10
a += 5
print("使用+=后的值a:", a)  # 15
a -= 3
print("使用-=后的值a:", a)  # 12
a *= 2
print("使用*=后的值a:", a)  # 24
a /= 4
print("使用/=后的值a:", a)  # 6.0
a %= 4
print("使用%=后的值a:", a)  # 2.0
a //= 1.5
print("使用//=后的值a:", a)  # 1.0
a **= 3
print("使用**=后的值a:", a)  # 1.0  
# 总结:
## 增强赋值运算符结合了基本的算术运算符和赋值运算符，用于简化对变量的更新操作
# 例如，a += 5 等同于 a = a + 5
## 常见的增强赋值运算符包括：+=、-=、*=、/=、%=、//= 和 **=

# 比较运算符
x = 10
y = 20
print("x 等于 y:", x == y)  # False
print("x 不等于 y:", x != y)  # True
print("x 大于 y:", x > y)  # False
print("x 小于 y:", x < y)  # True
print("x 大于等于 y:", x >= y)  # False
print("x 小于等于 y:", x <= y)  # True

# 总结:
## 比较运算符用于比较两个值，并返回布尔值（True 或 False）
## 常见的比较运算符包括：==、!=、>、<、>= 和 <=

# 逻辑运算符
p = True
q = False
print("p 和 q:", p and q)  # False
print("p 或 q:", p or q)  # True
print("非 p:", not p)  # False
# 总结:
## 逻辑运算符用于组合多个布尔表达式
## 常见的逻辑运算符包括：and、or 和 not

# 位运算符
m = 5  # 二进制: 0101
n = 3  # 二进制: 0011
print("m 与 n (AND):", m & n)  # 1 (二进制: 0001)
print("m 或 n (OR):", m | n)  # 7 (二进制: 0111)
print("m 异或 n (XOR):", m ^ n)  # 6 (二进制: 0110)
print("m 左移 1 位:", m << 1)  # 10 (二进制: 1010)
print("n 右移 1 位:", n >> 1)  # 1 (二进制: 0001)
# 总结:
## 位运算符用于对整数的二进制位进行操作
## 常见的位运算符包括：&（与）、|（或）、^（异或）、<<（左移）和 >>（右移）

